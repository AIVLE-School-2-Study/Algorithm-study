# 입력값 설명
# 첫째 줄에 한울이가 가지고 있는 신발의 개수 N개가 주어집니다. (1 ≤ N ≤ 1,000)
# 둘째 줄부터 N개 줄에는 옷의 번호 K가 주어집니다. (1 ≤ K ≤ 1,000)

# 출력값 설명
# 한울이가 가장 많이 가지고 있는 신발의 번호를 출력합니다.
# 가장 많이 가지고 있는 신발이 여러 개라면, 그 중에서 가장 번호가 큰 신발 번호를 출력하세요.

import sys
sys.setrecursionlimit(1000)

N = int(sys.stdin.readline())

K = [0] * N

for _ in range(N):
    k = int(sys.stdin.readline())
    K[k - 1] += 1

K_max = 0

for i in range(1, N):
    if K[K_max] <= K[i]:
        K_max = i

print(K_max + 1)

