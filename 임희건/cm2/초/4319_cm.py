# 한울이는 신발 매장을 영업하고 있습니다.
# 한울이는 요즘 장사가 잘 되지 않자 가장 먼저 자신의 매장에 남아있는 신발의 재고를 확인하기로 했습니다.
# 각 신발에는 번호가 붙어 있으며 서로 다른 신발에는 다른 번호가 붙어있습니다.
# 같은 종류의 신발에는 같은 번호가 붙어있습니다.
# 한울이는 가장 안 팔리는 신발이 어떤 신발인지 알아보고 창고로 옮기기 위해 매장 안의 모든 신발을 모아 번호를 확인하려고 합니다.
# 따라서 번호를 확인하여 가장 재고가 많이 남아있는 신발이 무엇인지 찾아내는 프로그램을 작성해주세요.

